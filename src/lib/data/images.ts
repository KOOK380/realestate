export function imagesFor(type: string): string[] {
  const pool =
    type === "Villa" || type === "Independent House"
      ? LUXURY_VILLA_IMAGES
      : type === "Penthouse"
      ? [...APARTMENT_IMAGES, ...INTERIOR_IMAGES]
      : type === "Plot"
      ? PLOT_IMAGES
      : type.startsWith("Commercial") || type === "Retail Shop"
      ? COMMERCIAL_IMAGES
      : APARTMENT_IMAGES;
  // interleave with interior shots for variety
  return [...pool, ...INTERIOR_IMAGES];
}
